class DremioDialect(DefaultDialect):
    preparer = DremioIdentifierPreparer


class DremioExprTranslator(AlchemyExprTranslator):
    _registry = operation_registry.copy()

    @cached_property
    def dialect(self) -> sa.engine.interfaces.Dialect:
        return DremioDialect()


class DremioCompiler(AlchemyCompiler):
    translator_class = DremioExprTranslator
